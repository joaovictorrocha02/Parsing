package Parsing01;

public class Parsing01 {
    public static void main(String[] args) {
       
        String shirtPrice = "15";
        int intVar1 = Integer.parseInt(shirtPrice);
        System.out.println(intVar1);
        
        String textRate = "0.05";
        double doubleVar2 = Double.parseDouble(textRate);
        System.out.println(doubleVar2);
        
        String gibberish = "88ds7nds87dsfs";
        System.out.println(gibberish);
        
        
        
        
     
        
    }
    
}
